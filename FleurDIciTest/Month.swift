    case february = "Février"
    case march = "Mars"
    case april = "Avril"
    case may = "Mai"
    case june = "Juin"
    case july = "Juillet"
    case september = "Septembre"
    case october = "Octobre"
    case november = "Novembre"
    case december = "Décembre"
}
